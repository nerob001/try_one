package ProgrammingForTT;

public class SWcase {

    public static void main(String[] args) {

        int n = 5;

        switch(n){

            case 1 :

                System.out.println("Sun");
                break;

            case 2:
                System.out.println("Mon");
                break;

            default:
                System.out.println("random");

        }

    }

}
