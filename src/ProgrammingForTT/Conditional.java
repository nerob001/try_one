package ProgrammingForTT;

public class Conditional {
    public static void main(String[] args) {

        float a = 80.0f;

        if(a>=80){

            System.out.println("A+");

        }

        else if(a>=60){

            System.out.println("A");

        }

        else if(a>=50){

            System.out.println("B");

        }

        else if(a>=40){

            System.out.println("C");

        }

        else{

            System.out.println("F");
        }
    }
}
