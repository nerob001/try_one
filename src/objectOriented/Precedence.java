package objectOriented;

public class Precedence {
    public static void main(String[] args) {

        int ab = 6*5 - 34/2;
        System.out.println(ab);
        int n = 6/5 - 34*2;
        System.out.println(n);

        int a = 2 ,b = 4 ,c = 5;
        float F = (b*b - 4*a*c)/(2*a);

        System.out.println(F);
    }
}
